# TYPES OF OPERATORS
# Arithmetic operators
# Assignment operators
# Comparison operators
# Logical operators


# 1- Arithmetic Operators
a=3
b=4
print("The value of 3+4 is",a+b)
print("The value of 3+4 is",3+4)
print("The value of 3-4 is",3-4)
print("The value of 3*4 is",3*4)
print("The value of 3/4 is",3/4)


# 2- Assignment Operators
a = 30
a += 5
print(a)
b = 30
b -= 5
print(b)
c = 30
c *= 5
print(c)
d = 30
d /= 5
print(d)
f = 30
f += 5
f -= 5
f *= 5
f /= 5
print(f)


# 3- Comparision Operators
a = (3<7)
print(a)
b = (7>7)
print(b)
c = (7>=7)
print(c)
d = (7==7)
print(d)
e = (9!=7)     # mean 9 not equal to 7   [ ! MEANS NOT ]
print(e)


# 4- Logical Operators [ and, not, or ]
a = False
b = True
print("The value of a and b is",( a and b ))               # both ture so ture otherwise FALSE
print("The value of a or b is",( a or b ))                 # both false so false otherwise TRUE
print("The value of not b is",( not b ))


