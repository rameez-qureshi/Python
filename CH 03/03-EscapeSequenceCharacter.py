
#  \n  insert a new line
story = "Ertugrul is good.\n Usman is also very good"      # \a \b \r \f
print(story)

#  \t  insert a empty space
story = "Ertugrul is good.\t Usman is also very good"
print(story)

#  \'  insert a single quote
story = "Ertugrul is good. \"Usman\" is also very good"
print(story)

#  \\  insert a back slash
story = "Ertugrul is good.\\ Usman is also very good"
print(story)